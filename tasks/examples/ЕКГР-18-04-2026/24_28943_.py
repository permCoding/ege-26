s = 'PA20EZ8TXTFZCZ220IS20QRD'  # 20
s = s.replace('20','#')
for c in 'EIOUY': s = s.replace(c, 'A')
print("s =", s)

amount = 2  # 26 !-!-!-!-!-!-!-!-!-!-!-!_____________________26
cnt, min_len = 0, 10**9
r = len(s) - 1
while r > amount * 2:
    while s[r] != 'A': r -= 1
    l = r
    while cnt < amount:
        l -= 1
        if s[l] == '#': cnt += 1
    print(s[l:r+1])
    if s[l:r].count('A') == 0:
        min_len = min(min_len, r - l + 1)
        print(l, r, s[l:r+1])
    r -= 1
print(min_len+amount)

